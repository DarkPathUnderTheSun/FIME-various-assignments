puts "\n####Programa que convierte de Grados Faharenheit a Grados Celcius###.\n\n"
for f in (0..300).step(20)
    c=((f-32)/1.8)
    puts "\n#{f} Faharenheit es igual a #{c.to_i} grados celcius. \n"
end

